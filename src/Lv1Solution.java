public class Lv1Solution {

    // 1. 두 수의 차
    public int solution1(int num1, int num2) {
        int answer = num1 - num2;
        return answer;
    }

    // 2. 두 수의 곱
    public int solution2(int num1, int num2) {
        int answer = num1 * num2;
        return answer;
    }

    // 3. 몫 구하기
    public int solution3(int num1, int num2) {
        int answer = num1 / num2;
        return answer;
    }
}
